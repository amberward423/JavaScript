const n = prompt('Enter a number:  ');
const m =  prompt('Enter a second number:  ');
const o =  prompt('Enter a third number:  ');


const N = parseInt(n)
const M = parseInt(m)
const O = parseInt(o)

const sum = N+M+O

const average = (N+M+O)/3

const product =  N*M*O

document.write("The sum is: " +sum )
document.write("The average is: " +average)
document.write("The product is: " +product)

